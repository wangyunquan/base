package pojo;

public class SubFileInfo {
	private String name;
	private String subFileSize;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSubFileSize() {
		return subFileSize;
	}

	public void setSubFileSize(String subFileSize) {
		this.subFileSize = subFileSize;
	}

}
